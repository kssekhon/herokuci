	public function index()
	{
		$this->db->query('CREATE TABLE weather (
    city            varchar(80),
    temp_lo         int,           -- low temperature
    temp_hi         int,           -- high temperature
    prcp            real,          -- precipitation
    date            date
)');

		$this->load->view('welcome_message');
		
	}
